class UserLoginForm(AuthenticationForm):
    username = forms.CharField(label='Имя пользователя', widget=forms.TextInput(attrs={'class': 'form-control'}))
    password = forms.CharField(label='Пароль', widget=forms.PasswordInput(attrs={'class': 'form-control'}))

class UserFeedBackForm(forms.Form):
    name = forms.CharField(max_length=60, label='Имя', widget=forms.TextInput(attrs={'class': 'form-control'}))
    mail = forms.EmailField(label='E-mail', widget=forms.EmailInput(attrs={'class': 'form-control'}))
    subject = forms.CharField(max_length=60, label='Тема', widget=forms.TextInput(attrs={'class': 'form-control'}))
    content = forms.CharField(label='Обращение', widget=forms.Textarea(attrs={'class': 'form-control', 'rows': 5}))